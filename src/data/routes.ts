const scene = (
  route: RouteId,
  suffix: "a" | "b",
  biome: string,
  time: TimePreset,
  weather: WeatherPreset[],
  palette: string[],
): SceneAsset => ({
  id: `${route}-${suffix}`,
  route,
  biome,
  type: "layered-image",
  sources: [
    {
      src: `/media/routes/${route}-${suffix}.avif`,
      type: "image/avif",
      width: 1600,
      height: 900,
    },
  ],
  poster: `/media/posters/${route}.webp`,
  duration: 32,
  loopStrategy: "crossfade",
  depthLayers: layers,
  dominantPalette: palette,
  naturalTime: time,
  supportedWeather: weather,
  attributionId: `generated-${route}`,
});

